package de.wi2020sebgroup1.cinemachatbot.rest;

public class Month {
	
	private String name;
	private String nr;
	
	public Month(String name, String nr) {
		this.name = name;
		this.nr = nr;
	}
	
	public String getName() {
		return name;
	}
	
	public String getNr() {
		return nr;
	}

}
